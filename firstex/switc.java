package firstex;

public class switc {

	public static void main(String[] args) {
		int j=7;
		switch(j) {
		case 0:
			System.out.println("value is 0");
			break;
		case 1:
			System.out.println("value is 1");
			break;
		case 2:
			System.out.println("value is 2");
			break;
		default:
			System.out.println("No Match");
            break;
		}

	}

}
